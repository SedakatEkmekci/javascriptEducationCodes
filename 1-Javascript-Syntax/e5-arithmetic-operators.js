let x = 5;
let y = 2;

/* Adding Operator */

let sum = x + y;
console.log("x:", x, "y:", y, "sum:", sum);

/* Substraction Operator */

let substraction = x - y;
console.log("x:", x, "y:", y, "substraction:", substraction);

/* Multiplication Operator */

let multiplication = x * y;
console.log("x:", x, "y:", y, "multiplication:", multiplication);

/* Division Operator */

let division = x / y; //çıkan sonuç bastırılır.Kalan bastırılmaz.
console.log("x:", x, "y:", y, "division:", division);

/* Modulus Operator */

let remainder = x % y; //kalan bastırılır
console.log("x:", x, "y:", y, "remainder:", remainder);

/* Increment */

x++; // x sayısına 1 ekler.
console.log("x:", x);

x = x + 1;
console.log("x:", x);

/* Decrement */

y--; // y'den 1 çıkartılır
console.log("y:", y);

y = y - 1;
console.log("y:", y);